    private void mostrarEstadoTemporal(String mensaje, Color color) {

        txtEstado.setForeground(color);
        txtEstado.setText(mensaje);

        javax.swing.Timer timer = new javax.swing.Timer(2000, e -> {
            txtEstado.setForeground(Color.BLUE);
            txtEstado.setText("En espera");
        });

        timer.setRepeats(false);
        timer.start();
    }
    
    private void centrarScrollArbol() {
        java.awt.EventQueue.invokeLater(() -> {
            javax.swing.JScrollBar barraHorizontal = scrollArbol.getHorizontalScrollBar();
            javax.swing.JScrollBar barraVertical = scrollArbol.getVerticalScrollBar();

            int centroX = (barraHorizontal.getMaximum() - barraHorizontal.getVisibleAmount()) / 2;

            barraHorizontal.setValue(Math.max(0, centroX));
            barraVertical.setValue(0);
        });
    }

