    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        
        String idTexto = txtId.getText().trim();
        String descripcion = txtDescripcion.getText().trim();
        String categoria = cmbCategoria.getSelectedItem().toString().trim();
        
        //Validacion de datos
        
        if(idTexto.isEmpty()||descripcion.isEmpty()||categoria.equalsIgnoreCase("Seleccione...")){
            mostrarEstadoTemporal("Todos los campos son obligatorios",Color.RED);
            return;
        }
        
        if (!descripcion.matches(".*[a-zA-Z].*")) {
            mostrarEstadoTemporal("El Campo descripcion debe contener Letras",Color.RED);
            return;
        }
        
        int id;
        
        try{
            
            id = Integer.parseInt(idTexto);
            
        }catch(NumberFormatException e){
            
            mostrarEstadoTemporal("El Id Debe ser un numero Entero",Color.RED);
            return;
        }
        
        if(id <= 0){
            mostrarEstadoTemporal("El Id Debe ser un numero Positivo",Color.RED);
            return;
        }
        
        //Se crea esta validacion para mejorar la parte grafica, no es un requerimiento del proyecto como tal.
        if(id > 999){
            mostrarEstadoTemporal("El Id no puede tener mas de 3 Digitos",Color.RED);
            return;
        }
        
        //Creacion de la clase Tarjeta e insercion en el arbol
        
        Entidad.Tarjeta tarjeta = new Entidad.Tarjeta(id, descripcion, categoria);
        
        if(arbol.insertar(tarjeta)){
            mostrarEstadoTemporal("Tarjeta Insertada Correctamente",Color.green);
            limpiarCampos();
            panelArbol.revalidate();
            panelArbol.setNodoResaltado(null);
            panelArbol.repaint();
            scrollArbol.revalidate();
            scrollArbol.repaint();
            centrarScrollArbol();
            txaDatos.setText("");
        }else{
            mostrarEstadoTemporal("El ID ya existe en el arbol",Color.RED);
        }
        
    }//GEN-LAST:event_btnInsertarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String idTexto = txtId.getText().trim();
        
        if(idTexto.isEmpty()){
            mostrarEstadoTemporal("Ingresar un ID para buscar", Color.RED);
            return;
        }
        
        int id;
        
        try{
            
            id = Integer.parseInt(idTexto);
            
        }catch(NumberFormatException e){
            
            mostrarEstadoTemporal("El ID debe ser un numero Entero",Color.RED);
            return;
        }
        if(id <= 0){
            mostrarEstadoTemporal("El ID debe ser un numero Positivo",Color.RED);
            return;
        }
        
        Nodo encontrado = arbol.buscar(id);
        
        if (encontrado != null){
            
            panelArbol.setNodoResaltado(encontrado);
            panelArbol.repaint();
            
            txaDatos.setText(                    
                "ID: " + encontrado.getDato().getId() +
                "\nDescripcion: " + encontrado.getDato().getDescripcion() +
                "\nCategoria: " + encontrado.getDato().getCategoria() +
                "\n" + "\n !!! Puedes ver la posicion del nodo en azul !!! "
            );
            
            mostrarEstadoTemporal("Nodo Encontrado", Color.GREEN);
        }else{
            
            panelArbol.setNodoResaltado(null);
            panelArbol.repaint();

            txaDatos.setText("");
            mostrarEstadoTemporal("ID no encontrado", Color.RED);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

