    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        
       String idTexto = txtId.getText().trim();

        if (idTexto.isEmpty()) {
            mostrarEstadoTemporal("Ingrese un ID para eliminar", Color.RED);
            return;
        }

        int id;

        try {
            id = Integer.parseInt(idTexto);
        } catch (NumberFormatException e) {
            mostrarEstadoTemporal("El ID debe ser un numero entero", Color.RED);
            return;
        }

        if (id <= 0) {
            mostrarEstadoTemporal("El ID debe ser un numero positivo", Color.RED);
            return;
        }

        String resultado = arbol.eliminar(id);

        if (resultado.equals("Nodo eliminado correctamente")) {
            panelArbol.setNodoResaltado(null);
            panelArbol.revalidate();
            panelArbol.repaint();
            scrollArbol.revalidate();
            scrollArbol.repaint();

            txaDatos.setText("");
            mostrarEstadoTemporal(resultado, Color.GREEN);
        } else {
            mostrarEstadoTemporal(resultado, Color.RED);
        } 
    }//GEN-LAST:event_btnEliminarActionPerformed

